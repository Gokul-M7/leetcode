class Solution {
    public boolean checkZeroOnes(String s) {
        int c1=0,c2=0,max1=0,max2=0;
        for(int i=0;i<s.length();i++)
        {
            if(s.charAt(i)=='1')
            {
                c1++;
                c2=0;
                max1=Math.max(c1,max1);
            }else
            {
                c2++;
                c1=0;
                max2=Math.max(c2,max2);
            }
        }
        return max1>max2;
    }
}